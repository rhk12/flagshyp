%--------------------------------------------------------------------------
% Obtain quadrature (numerical integration) rules for internal elements. 
%--------------------------------------------------------------------------
function QUADRATURE = element_quadrature_rules(element_type)
switch element_type
    case 'tria3'
         QUADRATURE.Chi = [1/3 1/3];
         QUADRATURE.W   = 0.5;
    case 'quad4'
        QUADRATURE.Chi  = [[-0.577350269189626  -0.577350269189626];
                           [ 0.577350269189626  -0.577350269189626];
                           [ 0.577350269189626   0.577350269189626];
                           [-0.577350269189626   0.577350269189626]];
        QUADRATURE.W    = [1;1;1;1];
    case 'tria6'
        QUADRATURE.Chi  = [[0.5 0];[0.5 0.5];[0 0.5]];
        QUADRATURE.W    = [1/6;1/6;1/6];
    case 'tetr4'       
        QUADRATURE.Chi  = [0.25 0.25 0.25];
        QUADRATURE.W    = 1/6;
    case 'tetr10'    
        QUADRATURE.Chi  = [[0.25;0.25;0.25];
                           [1/6;1/6;1/6];
                           [0.5;1/6;1/6];
                           [1/6;0.5;1/6];   
                           [1/6;1/6;0.5]];       
        QUADRATURE.W    = [-0.1333333333333333; 0.075; 0.075; 0.075; 0.075];        
    case 'hexa8'        
        QUADRATURE.Chi  = [[-0.577350269189626  -0.577350269189626  -0.577350269189626];
                           [ 0.577350269189626  -0.577350269189626  -0.577350269189626];
                           [ 0.577350269189626   0.577350269189626  -0.577350269189626];
                           [-0.577350269189626   0.577350269189626  -0.577350269189626];
                           [-0.577350269189626  -0.577350269189626   0.577350269189626];
                           [ 0.577350269189626  -0.577350269189626   0.577350269189626];
                           [ 0.577350269189626   0.577350269189626   0.577350269189626];
                           [-0.577350269189626   0.577350269189626   0.577350269189626]];        
       QUADRATURE.W     = [1;1;1;1;1;1;1;1];                      
end
%--------------------------------------------------------------------------
% Set the number of integration points.
%--------------------------------------------------------------------------
QUADRATURE.ngauss = size(QUADRATURE.W,1);  
end
